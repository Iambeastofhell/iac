function loadImages() {
    const organisingTeamContainer = document.getElementById('organizing-team');
    organisingTeam.forEach(data => {
        const colDiv = document.createElement('div');
        colDiv.classList.add('col-lg-3', 'col-md-4', 'col-sm-6', 'mb-4');
        colDiv.innerHTML = `
            <div class="image-container">
                <img src="${data.imgSrc}" class="img-fluid" alt="${data.name}">
                <div class="overlay">
                    <div class="text">
                        <h5>${data.name}</h5>
                        <p class="post">${data.post}</p> <!-- Add class to the second line -->
                    </div>
                </div>
            </div>
        `;
        organisingTeamContainer.appendChild(colDiv);
    });

    const departmentCoordinatorsContainer = document.getElementById('department-cordinators');
    departmentCoordinators.forEach(data => {
        const colDiv = document.createElement('div');
        colDiv.classList.add('col-lg-3', 'col-md-4', 'col-sm-6', 'mb-4');
        colDiv.innerHTML = `
            <div class="image-container">
                <img src="${data.imgSrc}" class="img-fluid" alt="${data.name}">
                <div class="overlay">
                    <div class="text">
                        <h5>${data.name}</h5>
                        <p class="post">${data.post}</p> <!-- Add class to the second line -->
                    </div>
                </div>
            </div>
        `;
        departmentCoordinatorsContainer.appendChild(colDiv);
    });

    const eventSpeakersContainer = document.getElementById('event-speakers');
    speakers.forEach(data => {
        const speaker = `
            <div class="speaker__card">
        <div class="front">
            <div class="main__card">
            <img src="${data.imgSrc}" alt="" />
            <div class="overlay__container">
                <div class="text__container">
                <h1>${data.name}</h1>
                <p>${data.post}</p>
                </div>
            </div>
            </div>
        </div>
        <div class="back main__card">
            <img src="${data.imgSrc}" alt="" />
            <div class="overlay__container details">
            <div class="text__container">
                <p>${data.details}</p>
            </div>
            </div>
        </div>
        </div>
    `;
        eventSpeakersContainer.innerHTML += speaker;
    });
}


